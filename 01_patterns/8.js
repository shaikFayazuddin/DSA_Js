// 0th row - 9
// 1-7
// 2-5
// 3-3
// 4-1

function pattern(n){

  for(let i=0;i<n;i++){
    let row = ""

    for(let j=0;j<i;j++){
      row += " "
    }

    for(let j=0;j<n*2-(2*i+1);j++){
      row += "*"
    }

    for(let j=0;j<i;j++){
      row += " "
    }


    console.log(row)

  }
}


pattern(9)